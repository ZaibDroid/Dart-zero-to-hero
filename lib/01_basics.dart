// Basics — main(), Output, Comments
// ==================================


/*The main() function is the ENTRY POINT of every Dart program.
When you run a Dart file, execution starts from main().*/


// OUTPUT — print() vs stdout.write()
// ------------------------------------
/*print()          → prints text + moves to next line
stdout.write()   → prints text WITHOUT moving to next line*/


// COMMENTS
// ----------
/*1. Single line comment  →  use //
2. Multi line comment   →  use /* ... */
*/




void main() {

  print('Hello World');         // Hello World
  print('Welcome to Dart!');    // Welcome to Dart!

  print(7 + 3);   // 10  (math inside print works too)
  print(7 * 3);   // 21

}